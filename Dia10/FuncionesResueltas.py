def RondasGanadas(J1,J2):
    numRondas=[0,0]
    for i in range(len(J1)):
        if J1[i] > J2[i]:
            numRondas[0]+=1
        elif J1[i] < J2[i]:
            numRondas[1]+=1
    return numRondas

def PorcentajeVictorias(J1,J2):
    pj1,pj2 = RondasGanadas(J1,J2)
    p1=(pj1/len(J1))*100
    p2=(pj2/len(J1))*100
    return [p1,p2]


